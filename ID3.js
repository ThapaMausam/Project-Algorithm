/**
 * Calculates generic classification metrics (Accuracy, Precision, Recall).
 */
function calculateMetrics(matrix, classes) {
  let totalCorrect = 0;
  let totalExamples = 0;

  // Aggregate data
  for (const aClass of classes) {
    for (const pClass of classes) {
      const count = matrix[aClass][pClass] || 0;
      totalExamples += count;
      if (aClass === pClass) {
        totalCorrect += count; // Diagonal is correct predictions
      }
    }
  }

  const accuracy = totalExamples > 0 ? totalCorrect / totalExamples : 0;

  const classMetrics = {};

  for (const targetClass of classes) {
    // True Positive (TP): Actual is targetClass AND Predicted is targetClass
    const TP = matrix[targetClass][targetClass] || 0;

    // False Positive (FP): Actual is NOT targetClass BUT Predicted IS targetClass
    let FP = 0;
    for (const aClass of classes) {
      if (aClass !== targetClass) {
        FP += matrix[aClass][targetClass] || 0;
      }
    }

    // False Negative (FN): Actual IS targetClass BUT Predicted is NOT targetClass
    let FN = 0;
    for (const pClass of classes) {
      if (pClass !== targetClass) {
        FN += matrix[targetClass][pClass] || 0;
      }
    }

    // Precision: TP / (TP + FP) -> How many predicted as targetClass were correct?
    const precisionDenominator = TP + FP;
    const precision = precisionDenominator > 0 ? TP / precisionDenominator : 0;

    // Recall: TP / (TP + FN) -> How many actual targetClass were correctly identified?
    const recallDenominator = TP + FN;
    const recall = recallDenominator > 0 ? TP / recallDenominator : 0;

    classMetrics[targetClass] = { TP, FP, FN, precision, recall };
  }

  return { accuracy, totalCorrect, totalExamples, classMetrics };
}

