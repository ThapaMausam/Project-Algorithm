// Helper function to format the matrix nicely
function printMatrix(matrix, classes) {
  // 1. Print Header Row
  let header = "ACTUAL \\ PREDICTED |";
  for (const pClass of classes) {
    header += ` ${pClass.padEnd(5)} |`; // Pad with spaces for alignment
  }
  console.log(header);
  console.log("-".repeat(header.length));

  // 2. Print Data Rows
  for (const aClass of classes) {
    let row = `${aClass.padEnd(20)}|`;
    for (const pClass of classes) {
      const count = matrix[aClass][pClass] || 0;
      row += ` ${String(count).padEnd(5)} |`;
    }
    console.log(row);
  }
}

