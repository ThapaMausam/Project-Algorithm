/*
 * Builds a generic Confusion Matrix for a multi-class classifier.
 * @param {Object} tree The trained ID3 decision tree structure.
 * @param {Array<Object>} testData The dataset to evaluate the model on.
 * @param {string} targetAttr The name of the target/class attribute.
 * @returns {Object} A structure representing the confusion matrix.
 */
function buildConfusionMatrix(tree, testData, targetAttr) {
  // 1. Identify all unique class labels
  const allActualClasses = new Set(testData.map((d) => d[targetAttr]));
  // We can also get all classes from the training set for completeness, but test set classes are the minimum needed.
  const uniqueClasses = Array.from(allActualClasses).sort(); // Sort for consistent order

  // 2. Initialize the Matrix structure
  // We'll use a nested object (Map) for easy access: matrix[Actual][Predicted]
  const matrix = {};
  for (const actualClass of uniqueClasses) {
    matrix[actualClass] = {};
    for (const predictedClass of uniqueClasses) {
      matrix[actualClass][predictedClass] = 0;
    }
  }

  // 3. Populate the Matrix by classifying each test example
  for (const example of testData) {
    const actual = example[targetAttr];
    // Note: We need the classify function from the previous step here.
    const predicted = classify(tree, example);

    // Handle a predicted class that might not be in the initial uniqueClasses set
    if (!matrix[actual] || !matrix[actual][predicted]) {
      // If 'predicted' class is new, add it to the matrix structure (robustness)
      if (matrix[actual]) {
        matrix[actual][predicted] = (matrix[actual][predicted] || 0) + 1;
      } else {
        // This case should not happen if all classes are captured, but it handles edge cases.
        // For simplicity, we assume 'actual' is always in the set.
        console.error(`Actual class '${actual}' not initialized.`);
      }
    } else {
      // Increment the count at the intersection of Actual (row) and Predicted (column)
      matrix[actual][predicted] += 1;
    }
  }

  return { matrix, uniqueClasses }; // Return classes to aid in printing/analysis
}

