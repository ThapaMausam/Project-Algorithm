function evaluateTwoStageSystem(testData) {
  console.log("\n\n========================================");
  console.log("TWO-STAGE SYSTEM EVALUATION");
  console.log("========================================");

  let TierCorrect = 0;
  let CollegeCorrect = 0;
  let bothCorrect = 0;
  const total = testData.length;

  // Confusion matrices
  const TierConfusion = {};
  const CollegeConfusion = {};

  // Track results
  const results = [];

  for (let i = 0; i < testData.length; i++) {
    const example = testData[i];
    const actualTier = example.Tier;
    const actualCollege = example.College;

    // Make prediction
    const prediction = predictCollegeWithTier(example);
    const predictedTier = prediction.predictedTier;
    const predictedCollege = prediction.predictedCollege;

    // Track Tier accuracy
    if (predictedTier === actualTier) {
      TierCorrect++;
    }

    // Track College accuracy
    if (predictedCollege === actualCollege) {
      CollegeCorrect++;
    }

    // Track both correct
    if (predictedTier === actualTier && predictedCollege === actualCollege) {
      bothCorrect++;
    }

    // Build confusion matrix for Tier
    if (!TierConfusion[actualTier]) {
      TierConfusion[actualTier] = {};
    }
    TierConfusion[actualTier][predictedTier] =
      (TierConfusion[actualTier][predictedTier] || 0) + 1;

    // Build confusion matrix for College
    if (!CollegeConfusion[actualCollege]) {
      CollegeConfusion[actualCollege] = {};
    }
    CollegeConfusion[actualCollege][predictedCollege] =
      (CollegeConfusion[actualCollege][predictedCollege] || 0) + 1;

    results.push({
      index: i + 1,
      actualTier,
      predictedTier,
      TierMatch: predictedTier === actualTier ? "✓" : "✗",
      actualCollege,
      predictedCollege,
      CollegeMatch: predictedCollege === actualCollege ? "✓" : "✗",
    });
  }

  // Calculate accuracies
  const TierAccuracy = (TierCorrect / total) * 100;
  const CollegeAccuracy = (CollegeCorrect / total) * 100;
  const overallAccuracy = (bothCorrect / total) * 100;

  console.log("\n--- STAGE 1: Tier PREDICTION RESULTS ---");
  console.log(`Total Test Cases: ${total}`);
  console.log(`Correct Tier Predictions: ${TierCorrect}`);
  console.log(`Tier Accuracy: ${TierAccuracy.toFixed(2)}%`);

  console.log("\n--- STAGE 2: College PREDICTION RESULTS ---");
  console.log(`Correct College Predictions: ${CollegeCorrect}`);
  console.log(`College Accuracy: ${CollegeAccuracy.toFixed(2)}%`);

  console.log("\n--- OVERALL TWO-STAGE ACCURACY ---");
  console.log(`Both Tier and College Correct: ${bothCorrect}`);
  console.log(`Overall System Accuracy: ${overallAccuracy.toFixed(2)}%`);

  // Print detailed results table
  console.log("\n--- DETAILED PREDICTION RESULTS ---");
  console.log(
    "Test | Actual Tier | Pred Tier | ✓/✗ | Actual College | Pred College | ✓/✗"
  );
  console.log("-".repeat(90));
  for (const r of results) {
    console.log(
      `${String(r.index).padStart(4)} | ${r.actualTier.padEnd(
        11
      )} | ${r.predictedTier.padEnd(9)} | ${r.TierMatch.padEnd(
        3
      )} | ${r.actualCollege.padEnd(14)} | ${(
        r.predictedCollege || "NULL"
      ).padEnd(12)} | ${r.CollegeMatch}`
    );
  }

  // Print Tier confusion matrix
  console.log("\n--- STAGE 1: Tier CONFUSION MATRIX ---");
  printConfusionMatrix(TierConfusion, ["Top", "Mid", "Low"]);

  // Print College confusion matrix
  console.log("\n--- STAGE 2: College CONFUSION MATRIX ---");
  const allColleges = [...new Set(testData.map((d) => d.College))].sort();
  printConfusionMatrix(CollegeConfusion, allColleges);

  // Calculate per-class metrics for Tier
  console.log("\n--- STAGE 1: Tier METRICS PER CLASS ---");
  const TierMetrics = calculatePerClassMetrics(TierConfusion, [
    "Top",
    "Mid",
    "Low",
  ]);
  printPerClassMetrics(TierMetrics);

  // Calculate per-class metrics for College
  console.log("\n--- STAGE 2: College METRICS PER CLASS ---");
  const CollegeMetrics = calculatePerClassMetrics(
    CollegeConfusion,
    allColleges
  );
  printPerClassMetrics(CollegeMetrics);

  return {
    TierAccuracy,
    CollegeAccuracy,
    overallAccuracy,
    TierCorrect,
    CollegeCorrect,
    bothCorrect,
    total,
    results,
  };
}

function printConfusionMatrix(matrix, classes) {
  let header = "ACTUAL \\ PREDICTED |";
  for (const pClass of classes) {
    header += ` ${pClass.padEnd(12)} |`;
  }
  console.log(header);
  console.log("-".repeat(header.length));

  for (const aClass of classes) {
    let row = `${aClass.padEnd(18)}|`;
    for (const pClass of classes) {
      const count = matrix[aClass] ? matrix[aClass][pClass] || 0 : 0;
      row += ` ${String(count).padEnd(12)} |`;
    }
    console.log(row);
  }
}

function calculatePerClassMetrics(matrix, classes) {
  const metrics = {};

  for (const targetClass of classes) {
    const TP = matrix[targetClass] ? matrix[targetClass][targetClass] || 0 : 0;

    let FP = 0;
    for (const aClass of classes) {
      if (aClass !== targetClass && matrix[aClass]) {
        FP += matrix[aClass][targetClass] || 0;
      }
    }

    let FN = 0;
    if (matrix[targetClass]) {
      for (const pClass of classes) {
        if (pClass !== targetClass) {
          FN += matrix[targetClass][pClass] || 0;
        }
      }
    }

    const precision = TP + FP > 0 ? TP / (TP + FP) : 0;
    const recall = TP + FN > 0 ? TP / (TP + FN) : 0;
    const f1 =
      precision + recall > 0
        ? (2 * precision * recall) / (precision + recall)
        : 0;

    metrics[targetClass] = { TP, FP, FN, precision, recall, f1 };
  }

  return metrics;
}

function printPerClassMetrics(metrics) {
  console.log(
    "Class            | TP  | FP  | FN  | Precision | Recall   | F1-Score"
  );
  console.log("-".repeat(75));
  for (const [className, m] of Object.entries(metrics)) {
    console.log(
      `${className.padEnd(16)} | ${String(m.TP).padEnd(3)} | ${String(
        m.FP
      ).padEnd(3)} | ${String(m.FN).padEnd(3)} | ${m.precision
        .toFixed(4)
        .padEnd(9)} | ${m.recall.toFixed(4).padEnd(8)} | ${m.f1.toFixed(4)}`
    );
  }
}

