// Cache for Tier-specific College trees (to avoid rebuilding)
const CollegeTreeCache = {};

/**
 * Filters training data by a specific Tier value
 */
function filterDataByTier(data, TierValue) {
  return data.filter((example) => example.Tier === TierValue);
}

/**
 * Builds a College prediction tree for a specific Tier
 */
function buildCollegeTreeForTier(TierValue) {
  // Check cache first
  if (CollegeTreeCache[TierValue]) {
    console.log(`  → Using cached College tree for Tier: ${TierValue}`);
    return CollegeTreeCache[TierValue];
  }

  console.log(`  → Building new College tree for Tier: ${TierValue}`);

  // Filter training data to only this Tier
  const filteredData = filterDataByTier(trainingData, TierValue);

  if (filteredData.length === 0) {
    console.warn(`  → No training data found for Tier: ${TierValue}`);
    return null;
  }

  // Build tree with same attributes (excluding Tier and College)
  const CollegeAttributes = TierAttributes.filter((attr) => attr !== "Tier");

  const CollegeTree = buildID3Tree(filteredData, CollegeAttributes, "College");

  // Cache the tree
  CollegeTreeCache[TierValue] = CollegeTree;

  return CollegeTree;
}

/**
 * Main function: Predicts both Tier and College for a student
 */
function predictCollegeWithTier(studentData) {
  console.log("\n=== TWO-STAGE PREDICTION ===");
  console.log("Student Data:", studentData);

  // STAGE 1: Predict Tier
  const predictedTier = classify(TierTree, studentData);
  console.log(`\n[Stage 1] Predicted Tier: ${predictedTier}`);

  if (!predictedTier) {
    return {
      predictedTier: null,
      predictedCollege: null,
      error: "Could not predict Tier",
    };
  }

  // STAGE 2: Build/Get College tree for this Tier and predict
  const CollegeTree = buildCollegeTreeForTier(predictedTier);

  if (!CollegeTree) {
    return {
      predictedTier,
      predictedCollege: null,
      error: `No College tree available for Tier: ${predictedTier}`,
    };
  }

  const predictedCollege = classify(CollegeTree, studentData);
  console.log(`[Stage 2] Predicted College: ${predictedCollege}`);

  return {
    predictedTier,
    predictedCollege,
  };
}

