function performKFoldCrossValidation(
  fullData,
  k,
  featureAttributes,
  targetAttr,
  allClasses
) {
  console.log(`\n\n========================================`);
  console.log(`ðŸš€ STARTING ${k}-FOLD CROSS-VALIDATION`);
  console.log(`Total examples: ${fullData.length}`);
  console.log(
    `Pruning: maxDepth=${PRUNING_MAX_DEPTH}, minSamplesSplit=${PRUNING_MIN_SAMPLES_SPLIT}`
  );
  console.log(`Min Info Gain: ${MIN_INFORMATION_GAIN}`);
  console.log(`========================================`);

  shuffleArray(fullData);

  const foldSize = Math.ceil(fullData.length / k);
  const folds = [];
  for (let i = 0; i < fullData.length; i += foldSize) {
    folds.push(fullData.slice(i, i + foldSize));
  }
  if (folds.length > k) {
    const lastFold = folds.pop();
    folds[k - 1] = folds[k - 1].concat(lastFold);
  }

  let aggregatedCollegeConfusion = {};
  let totalCorrectCollege = 0;
  let totalProcessed = 0;
  let allTreeStats = [];

  for (let i = 0; i < folds.length; i++) {
    console.log(`\n--- FOLD ${i + 1} / ${k} ---`);

    const testData = folds[i];
    const trainingData = folds
      .slice(0, i)
      .concat(folds.slice(i + 1))
      .flat();
    console.log(
      `Training size: ${trainingData.length}, Test size: ${testData.length}`
    );

    if (trainingData.length === 0 || testData.length === 0) {
      console.warn("Skipping fold, not enough data for train/test split.");
      continue;
    }

    // Train Model
    const CollegeTree = buildID3Tree(
      trainingData,
      featureAttributes,
      targetAttr,
      PRUNING_MAX_DEPTH,
      PRUNING_MIN_SAMPLES_SPLIT
    );

    // âœ¨ NEW: Analyze tree structure
    const treeStats = analyzeTree(CollegeTree);
    allTreeStats.push(treeStats);
    console.log(
      `Tree: ${treeStats.nodes} nodes, ${treeStats.leaves} leaves, max depth: ${treeStats.maxDepth}`
    );

    // Evaluate
    const foldCollegeConfusion = {};
    let foldCollegeCorrect = 0;

    for (const example of testData) {
      const actualCollege = example[targetAttr];
      const predictedCollege = classify(CollegeTree, example);

      if (predictedCollege === actualCollege) foldCollegeCorrect++;

      if (!foldCollegeConfusion[actualCollege])
        foldCollegeConfusion[actualCollege] = {};
      foldCollegeConfusion[actualCollege][predictedCollege] =
        (foldCollegeConfusion[actualCollege][predictedCollege] || 0) + 1;
    }

    aggregateConfusionMatrix(aggregatedCollegeConfusion, foldCollegeConfusion);
    totalCorrectCollege += foldCollegeCorrect;
    totalProcessed += testData.length;

    const foldCollegeAcc = (foldCollegeCorrect / testData.length) * 100;
    console.log(
      `Fold ${i + 1} College Accuracy: ${foldCollegeAcc.toFixed(2)}%`
    );
  }

  // Report results
  console.log(`\n\n========================================`);
  console.log(`ðŸ“Š K-FOLD CROSS-VALIDATION SUMMARY`);
  console.log(`========================================`);

  const avgCollegeAcc = (totalCorrectCollege / totalProcessed) * 100;

  console.log(`\n--- OVERALL ACCURACY (MICRO-AVERAGE) ---`);
  console.log(`Total Predictions: ${totalProcessed}`);
  console.log(
    `College Accuracy: ${avgCollegeAcc.toFixed(
      2
    )}% (${totalCorrectCollege}/${totalProcessed})`
  );

  // âœ¨ NEW: Average tree statistics
  console.log(`\n--- AVERAGE TREE STATISTICS ---`);
  const avgNodes =
    allTreeStats.reduce((s, t) => s + t.nodes, 0) / allTreeStats.length;
  const avgLeaves =
    allTreeStats.reduce((s, t) => s + t.leaves, 0) / allTreeStats.length;
  const avgMaxDepth =
    allTreeStats.reduce((s, t) => s + t.maxDepth, 0) / allTreeStats.length;
  console.log(`Avg Nodes: ${avgNodes.toFixed(1)}`);
  console.log(`Avg Leaves: ${avgLeaves.toFixed(1)}`);
  console.log(`Avg Max Depth: ${avgMaxDepth.toFixed(1)}`);

  console.log("\n--- AGGREGATED COLLEGE CONFUSION MATRIX ---");
  printConfusionMatrix(aggregatedCollegeConfusion, allClasses);

  console.log("\n--- AGGREGATED COLLEGE METRICS PER CLASS ---");
  const CollegeMetrics = calculatePerClassMetrics(
    aggregatedCollegeConfusion,
    allClasses
  );
  printPerClassMetrics(CollegeMetrics);

  console.log("\n========================================");
  console.log("âœ… Cross-Validation Complete.");
  console.log("========================================");
}

