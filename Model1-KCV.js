/**
 * Finds the attribute with highest Information Gain.
 * âœ¨ FIXED: Now returns null if best gain is below threshold
 */
function findBestSplitAttribute(data, attributes, targetAttr) {
  let maxGain = -Infinity;
  let bestAttribute = null;

  for (const attribute of attributes) {
    const gain = calculateInformationGain(data, attribute, targetAttr);
    if (gain > maxGain) {
      maxGain = gain;
      bestAttribute = attribute;
    }
  }

  // âœ¨ FIX: Check if information gain is meaningful
  if (maxGain < MIN_INFORMATION_GAIN) {
    return null; // No attribute provides useful information
  }

  return bestAttribute;
}

// ============================================
// IMPROVED: buildID3Tree with better logging
// ============================================

function buildID3Tree(
  data,
  availableAttributes,
  targetAttr,
  maxDepth = 10,
  minSamplesSplit = 5,
  depth = 0 // Track current depth for debugging
) {
  // --- PRE-PRUNING BASE CASES ---
  if (maxDepth <= 0) {
    return {
      type: "leaf",
      class: getMajorityClass(data, targetAttr),
      reason: "max_depth",
      samples: data.length,
    };
  }

  if (data.length < minSamplesSplit) {
    return {
      type: "leaf",
      class: getMajorityClass(data, targetAttr),
      reason: "min_samples",
      samples: data.length,
    };
  }

  // --- ORIGINAL BASE CASES ---
  const entropy = calculateEntropy(data, targetAttr);
  if (entropy === 0) {
    return {
      type: "leaf",
      class: data[0][targetAttr],
      reason: "pure",
      samples: data.length,
    };
  }

  if (availableAttributes.length === 0) {
    return {
      type: "leaf",
      class: getMajorityClass(data, targetAttr),
      reason: "no_attributes",
      samples: data.length,
    };
  }

  // --- RECURSION ---
  const bestAttribute = findBestSplitAttribute(
    data,
    availableAttributes,
    targetAttr
  );

  // âœ¨ IMPROVED: Better handling of no information gain
  if (!bestAttribute) {
    return {
      type: "leaf",
      class: getMajorityClass(data, targetAttr),
      reason: "no_gain",
      samples: data.length,
      entropy: entropy.toFixed(4),
    };
  }

  const tree = {
    type: "node",
    attribute: bestAttribute,
    children: {},
    majorityClass: getMajorityClass(data, targetAttr),
    depth: depth,
    samples: data.length,
    entropy: entropy.toFixed(4),
  };

  const remainingAttributes = availableAttributes.filter(
    (attr) => attr !== bestAttribute
  );
  const subsets = splitData(data, bestAttribute);

  for (const [value, subset] of Object.entries(subsets)) {
    if (subset.length === 0) {
      tree.children[value] = {
        type: "leaf",
        class: tree.majorityClass,
        reason: "empty_subset",
        samples: 0,
      };
    } else {
      tree.children[value] = buildID3Tree(
        subset,
        remainingAttributes,
        targetAttr,
        maxDepth - 1,
        minSamplesSplit,
        depth + 1 // Increment depth
      );
    }
  }

  return tree;
}

// ============================================
// NEW: Tree Statistics Function
// ============================================

function analyzeTree(
  tree,
  stats = { leaves: 0, nodes: 0, maxDepth: 0, reasons: {} }
) {
  if (tree.type === "leaf") {
    stats.leaves++;
    const reason = tree.reason || "unknown";
    stats.reasons[reason] = (stats.reasons[reason] || 0) + 1;
    return stats;
  }

  stats.nodes++;
  stats.maxDepth = Math.max(stats.maxDepth, tree.depth || 0);

  for (const child of Object.values(tree.children)) {
    analyzeTree(child, stats);
  }

  return stats;
}

